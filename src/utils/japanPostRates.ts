export const getELightRate = (weightG: number, zone: JapanPostZone): number | null => {
  if (weightG <= 0 || weightG > 2000) return null;
  const idx = Math.ceil(weightG / 100) - 1;
  return E_LIGHT[zone][idx] ?? null;
};

// ── Mapeamento país → zona Japan Post ───────────────────────────────────────
export const countryToZone = (country: string): JapanPostZone => {
  const cfg = getCountryConfig(country);
  if (cfg) return cfg.zone;
  // Retrocompat
  switch (country) {
    case 'Brasil':         return 5;
    case 'Estados Unidos': return 4;
    case 'Portugal':
    case 'França':
    case 'Itália':
    case 'Espanha':        return 3;
    default:               return 5;
  }
};

