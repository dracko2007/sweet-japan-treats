/** Rate in ¥ for Air Parcel (1–30kg). Returns null if out of range. */
export const getAirParcelRate = (weightG: number, zone: JapanPostZone): number | null => {
  if (weightG <= 0 || weightG > 30000) return null;
  const idx = Math.ceil(weightG / 1000) - 1;
  return AIR_PARCEL[zone][Math.min(idx, 29)] ?? null;
};

