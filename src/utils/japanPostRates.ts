/** Rate in ¥ for EMS. Returns null if out of range. */
export const getEmsRate = (weightG: number, zone: JapanPostZone): number | null => {
  if (weightG <= 0 || weightG > 30000) return null;
  const idx = EMS_BRACKETS_G.findIndex(b => weightG <= b);
  if (idx === -1) return null;
  return EMS_RATES[zone][idx] ?? null;
};

